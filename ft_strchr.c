#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*ptr;

	ptr = (char *)s;
	while (*ptr != c && *ptr)
		ptr++;
	if (!*ptr && c != '\0')
		return (0);
	return (ptr);
}
